
public interface QuineMirrorable 
{
    /**
     * This method returns a string that contains the exact source code of the
     * class that implements it, but "upside down". i.e. the first line 
     * is the last, and the last line will be first.
     * @return Returns a String that contains an upside down version of 
     * the source code of the class that implements this method.
     */
    public String printSelfMirror();
}
